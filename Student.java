package com.mycompany.studenttimetablemanager;

public class Student {
    private String name;
    private String year;

    public Student(String name, String year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }
}
